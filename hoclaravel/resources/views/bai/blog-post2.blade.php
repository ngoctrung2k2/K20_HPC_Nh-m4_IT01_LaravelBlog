        <h1>Ban lãnh đạo HPC đón nhận Giấy chứng nhận đầu tư Dự án xây dựng Cơ sở Thanh Trì tại Hội nghị 
        “Hà Nội 2020 – Hợp tác đầu tư và phát triển”</h1> <br>

        <span> Thủ tướng Chính phủ Nguyễn Xuân Phúc, Phó Thủ tướng Phạm Bình Minh và các đồng chí lãnh đạo đại diện các bộ, 
            ban ngành trung ương và Thành phố Hà Nội; lãnh đạo các tỉnh, thành phố Vùng kinh tế trọng điểm Bắc Bộ, 
            Đồng bằng sông Hồng, Vùng Thủ đô; đại diện các cơ quan ngoại giao, tổ chức quốc tế và hơn 1.200 nhà đầu tư, 
            doanh nghiệp trong nước và quốc tế tham dự Hội nghị. </span> <br>

        <div class="text-center">
            <img src="/image/Su-kien-HPC-5.jpg" alt="" > <br>
        </div>

        <span>Hội nghị “Hà Nội 2020 – Hợp tác đầu tư và phát triển” là một trong chuỗi các hoạt động, 
            sự kiện quan trọng nhất của Thành phố Hà Nội đang triển khai nhằm thực hiện nhiệm vụ “kép”, 
            vừa phòng chống dịch, vừa phát triển kinh tế – xã hội. Đây cũng là một nội dung để chào mừng Đại hội đảng các cấp, 
            tiến tới Đại hội lần thứ XVII Đảng bộ Thành phố, Đại hội Đại biểu toàn quốc lần thứ XIII của Đảng, 
            Ông Nguyễn Đức Chung, Ủy viên T.Ư Đảng, Chủ tịch UBND TP bày tỏ.</span> <br>
        <div class="text-center">
            <img src="/image/sk-2.jpg" alt="" > <br>
        </div>
        <span>Phát biểu tại Hội nghị, Ủy viên Bộ Chính trị, Bí thư Thành ủy Vương Đình Huệ cho biết: Việc tổ chức Hội nghị “Hà Nội 2020 – Hợp tác Đầu tư và Phát triển” 
            sau khi dịch bệnh Covid-19 đã được kiểm soát và cơ bản được đẩy lùi sẽ là thông điệp mạnh mẽ của TP Hà Nội trong kiên trì thực hiện chỉ đạo của T.Ư về thực hiện nhiệm vụ “kép” 
            là vừa phòng chống dịch bệnh, vừa phát triển kinh tế – xã hội. Đồng thời khẳng định, Hà Nội là điểm đến an toàn, hấp dẫn cho các nhà đầu tư. Kết quả của Hội nghị không chỉ có ý nghĩa đối với sự phát triển kinh tế – xã hội 
            của Hà Nội và cả nước trong năm 2020 mà còn cho những năm tiếp theo.</span> <br>
            <div class="text-center">
                <img src="/image/sk-3.jpg" alt="" > <br>
            </div>
        <span>Tại Hội nghị, Thầy Bùi Quang Thịnh – Chủ tịch Hội đồng Giáo dục HPC đã vinh dự đón nhận Giấy chứng nhận đầu tư Dự án xây dựng Cơ sở Thanh Trì, do UBND TP Hà Nội cấp. Đây là một dấu mốc quan trọng trong kế hoạch xây dựng phát triển Trường Cao đẳng Công nghệ Bách khoa Hà Nội ngày càng lớn mạnh, không ngừng mở rộng và nâng cao chất lượng học tập cho sinh viên toàn trường.
            Tổng cộng đã có 299 dự án được cấp giấy chứng nhận đầu tư, với tổng số vốn: 405.570 tỷ đồng (tương đương 17,6 tỷ USD) với số vốn tăng thêm 270.458 tỷ đồng. Trong đó: 100 dự án trong nước, với số vốn 227.499 tỷ đồng (vốn tăng thêm 192.215 tỷ đồng); 22 dự án đầu tư vốn FDI, số vốn 5,7 tỷ USD, vốn tăng thêm 3,4 tỷ USD; và 107 dự án đầu tư công. Tổng số dự án, số vốn tăng, tương ứng gấp 5 lần và 11 lần so với Hội nghị xúc tiến đầu tư 2016. <br>
            Các dự án đầu tư này, tập trung vào các lĩnh vực: 25 cụm công nghiệp với 490,8ha; 800.000m2 nhà ở xã hội; 3 khu đô thị; Du lịch – dịch vụ; Trụ sở văn phòng; Văn hóa – xã hội; Tài chính – Ngân hàng; hạ tầng đô thị; hạ tầng giao thông;… <br>
            229 dự án được chia làm 3 nhóm: Nhóm các dự án trao Quyết định chủ trương tại Hội nghị gồm có 103 dự án với tổng số vốn 250.154 tỷ đồng; nhóm các dự án trao quyết định ghi nhận đề xuất tại Hội nghị có 19 dự án với tổng số vốn 109.849 tỷ đồng tỷ đồng (Những dự án này sẽ được Thành phố hoàn thiện hồ sơ trong quý III/2020); nhóm 107 dự án đầu tư công của TP đang được 5 Ban quản lý dự án TP và các quận, huyện, thị xã triển khai quyết liệt với số vốn 45.567 tỷ đồng. Phấn đấu hết quý III khởi công toàn bộ các dự án đầu tư công của TP. Đến 31/12/2020, hoàn thành 78/107 dự án. <br>
            Đồng thời cũng tại Hội nghị này, TP cùng các nhà đầu tư ký kết 38 Biên bản ghi nhớ với tổng mức đầu tư dự kiến khoảng 28,6 tỷ USD, trong đó: 26 đề xuất của nhà đầu tư trong nước (dự kiến khoảng 20,5 tỷ USD); 12 đề xuất của nhà đầu tư nước ngoài (dự kiến khoảng 8,32 tỷ USD).
        </span> <br>
        <div class="text-center">
            <img src="/image/sk-4.jpg" alt="" > <br>
        </div>
        <span>
            Bên cạnh đó, Ủy ban nhân dân thành phố Hà Nội cũng công bố 282 dự án xúc tiến, kêu gọi đầu tư với tổng số vốn 483,1 nghìn tỷ đồng (tương ứng 21,66 tỷ USD) trong 8 lĩnh vực (151 dự án công nghiệp, thương mại, dịch vụ; 9 dự án môi trường; 45 dự án hạ tầng xã hội; 13 dự án cải tạo, xây dựng lại chung cư cũ; 15 dự án nông nghiệp; dự án hạ tầng kỹ thuật;  5 dự án phát triển đô thị; 10 dự án phát triển nhà ở). <br>
            Thành phố Hà Nội cũng khen thưởng, vinh danh các cá nhân, tổ chức, doanh nghiệp trong nước và nước ngoài có thành tích đóng góp cho sự phát triển kinh tế – xã hội của Thủ đô. <br>
            Trong khuôn khổ của Hội nghị, các đại biểu đã tiến hành trao đổi, đề xuất các giải pháp thu hút hiệu quả các dự án hợp tác đầu tư trên địa bàn Hà Nội, nhằm đóng góp cho sự phát triển nhanh, bền vững của Thủ đô cũng như của cả nước trong thời gian tới. <br>
        </span>
